using UnityEngine;
//==============================================================================//
//                                 道具类型                                      //
//==============================================================================//
/// <summary>
/// 道具类型枚举（用于区分不同道具的功能）
/// </summary>
public enum ItemType
{
    None,       // 仅用于更新与排错
    All,        // 全部类型（用于批量操作）
    // 钥匙类
    Key_Red,    // 红钥匙
    Key_Blue,   // 蓝钥匙
    Key_Yellow, // 黄钥匙
    // 血瓶类
    HP_Potion,  // 血瓶
    // 宝石类
    Gem,        // 宝石
    // 幸运金币（存在则掉落金币翻倍）
    Lucky_Gold
}

/// <summary>
/// 道具加成数据结构（用于配置道具效果）
/// </summary>
[System.Serializable]
public class ItemBonus
{
    public ItemType type;   // 道具类型
    public int value;       // 道具效果值（如恢复量/数量）
}

//==============================================================================//
//                                 瓦片类型                                      //
//==============================================================================//
/// <summary>
/// 瓦片类型枚举（用于区分地图格子的功能）
/// </summary>
public enum GridType
{
    None,       // 空类型（排错用）
    Ground,     // 地面（可走）
    Wall,       // 墙（不可走）
    Item,       // 道具（可走，触发拾取）
    Enemy,      // 敌人（可走，触发战斗）
    Door,       // 门（需要钥匙开启）
    Stair,      // 楼梯（传送楼层）
    Event       // 事件（可走，触发剧情等）
}

//==============================================================================//
//                                 图层类型                                      //
//==============================================================================//
/// <summary>
/// 图层类型枚举（区分地图的不同功能层）
/// </summary>
public enum TileMapType
{
    None,           // 空类型（排错用）
    GroundWall,     // 基础层（地面和墙）
    Event           // 事件层（道具/敌人/楼梯等）
}

//==============================================================================//
//                                 属性类型                                      //
//==============================================================================//
/// <summary>
/// 属性类型枚举（玩家/敌人的基础属性）
/// </summary>
public enum AttributeType
{
    All,        // 初始化用（全属性更新）
    HP,         // 血量
    Attack,     // 攻击
    Defense,    // 防御
    Gold        // 金币
}

/// <summary>
/// 属性加成数据结构（用于配置属性变化）
/// </summary>
[System.Serializable]
public class AttributeBonus
{
    public AttributeType type;   // 属性类型
    public int value;            // 属性变化值
}

//==============================================================================//
//                                 楼梯类型                                      //
//==============================================================================//
/// <summary>
/// 楼梯类型枚举（区分楼梯的传送方向）
/// </summary>
public enum StairType
{
    UpStair,    // 上楼（当前楼层+1）
    DownStair,  // 下楼（当前楼层-1）
    Custom      // 自定义目标（任意楼层/位置）
}

/// <summary>
/// 出生来源枚举（区分玩家从哪个方向进入楼层）
/// </summary>
public enum SpawnSource
{
    FromLowerLayer, // 从下层上来（使用upSpawn）
    FromUpperLayer  // 从上层下来（使用downSpawn）
}
